
 
def String_concatination(str1,str2):
        
    concatinated_string= str1+ " "+ str2
    return concatinated_string

def String_slicing(text,start,end):
        
    sliced_string=text[start:end]
        
    return sliced_string


def String_formatting(name,age):
    formatted_string="My name is {} and my age is {}".format(name,age)

    return formatted_string
    
def To_Upper_case(text):
    uppercase=text.upper()
    return uppercase
    